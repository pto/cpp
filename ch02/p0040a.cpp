#include <iostream>

int main()
{
	std::cout << "sizeof(bool) is   " << sizeof(bool) << "\n";
	std::cout << "sizeof(char) is   " << sizeof(char) << "\n";
	std::cout << "sizeof(int) is    " << sizeof(int) << "\n";
	std::cout << "sizeof(double) is " << sizeof(double) << "\n";
	std::cout << "sizeof(long) is   " << sizeof(long) << "\n";
}
